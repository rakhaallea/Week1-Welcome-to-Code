// LOOPING PERTAMA
let n = 1;

console.log('MAJU')
while (n <= 10) {
    console.log(n)
    n++
}
console.log('MAJU AKHIR\n')

// LOOPING KEDUA
let k = 10;

console.log('MUNDUR')
while (k > 0) {
    console.log(k)
    k--
}
console.log('MUNDUR AKHIR')